export function createScheduleDayFromDraft(draft, existingDay) {
  return {
    id: existingDay?.id || draft.scheduleDayId || `day-${Date.now()}`,
    date: draft.date,
    title: draft.dayTitle || draft.engagementDetails || draft.eventTitle || draft.date,
  };
}

export function createMovementFromDraft(draft, scheduleDayId) {
  return {
    id: draft.id || `movement-${Date.now()}`,
    scheduleDayId,
    sortOrder: draft.sortOrder,
    driverId: draft.driverId,
    vehicleId: draft.vehicleId,
    driverStart: draft.driverStart,
    departureTime: draft.departureTime,
    arrivalTime: draft.arrivalTime,
    endTime: draft.endTime || draft.estimatedReturnTime || draft.eventEndTime,
    engagementDetails: draft.engagementDetails || draft.eventTitle,
    venue: draft.venue || draft.venueAddress,
    address: draft.address || draft.venueAddress,
    locationNotes: draft.locationNotes || draft.eventNotes,
    participants: draft.participants || draft.attendees,
    parking: draft.parking || draft.parkingInfo,
    internalNotes: draft.internalNotes || draft.generalNotes,
    isExecutiveVisible: draft.isExecutiveVisible,
    isOperationalVisible: draft.isOperationalVisible,
    eventStartTime: draft.eventStartTime || draft.departureTime,
    eventEndTime: draft.eventEndTime || draft.endTime,
    contactPerson: draft.contactPerson || "",
    contactPhone: draft.contactPhone || "",
    securityNotes: draft.securityNotes,
    protocolNotes: draft.protocolNotes,
    dressCode: draft.dressCode,
    documentsToCarry: draft.documentsToCarry,
    materialsOrGifts: draft.materialsOrGifts,
    specialInstructions: draft.specialInstructions,
  };
}

export function createDraftFromMovement(movement, day, profile) {
  return {
    ...emptyDraft,
    id: movement.id,
    scheduleDayId: movement.scheduleDayId,
    sortOrder: movement.sortOrder,
    dayTitle: day?.title || "",
    missionName: profile.missionName,
    documentTitle: profile.documentTitle,
    date: day?.date || "",
    driverId: movement.driverId || emptyDraft.driverId,
    vehicleId: movement.vehicleId || emptyDraft.vehicleId,
    driverStart: movement.driverStart || "",
    departureTime: movement.departureTime || "",
    arrivalTime: movement.arrivalTime || "",
    endTime: movement.endTime || "",
    engagementDetails: movement.engagementDetails || "",
    venue: movement.venue || "",
    address: movement.address || movement.venue || "",
    locationNotes: movement.locationNotes || "",
    parking: movement.parking || "",
    participants: movement.participants || "",
    internalNotes: movement.internalNotes || "",
    securityNotes: movement.securityNotes || "",
    protocolNotes: movement.protocolNotes || "",
    dressCode: movement.dressCode || "",
    documentsToCarry: movement.documentsToCarry || "",
    materialsOrGifts: movement.materialsOrGifts || "",
    specialInstructions: movement.specialInstructions || "",
    generalNotes: movement.internalNotes || "",
    isExecutiveVisible: movement.isExecutiveVisible !== false,
    isOperationalVisible: movement.isOperationalVisible !== false,
  };
}
